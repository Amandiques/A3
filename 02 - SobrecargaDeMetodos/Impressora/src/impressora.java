public class impressora {
    
    public void exibir(float num) {
        System.out.println(num);
    }
    
    public void exibir(float num1, float num2) {
        System.out.println(num1 + " " + num2);
    }
    
    public void exibir(float num, String texto) {
        System.out.println(num + " " + texto);
    }
    
    public void exibir(String texto, float num) {
        System.out.println(texto + " " + num);
    }
    
    public void exibir(String texto1, String texto2, String texto3) {
        System.out.println(texto1 + " " + texto2 + " " + texto3);
    }
    
    public void exibir(int num1, int num2, String texto) {
        System.out.println(num1 + " " + num2 + " " + texto);
    }
    

}
